function normalizeLang(raw: string): string {
  const value = (raw || 'en').toLowerCase();
  if (translations[value]) {
    return value;
  }
  if (value.includes('-')) {
    const base = value.split('-')[0];
    if (translations[base]) {
      return base;
    }
  }
  return 'en';
}

function resolveInitialLanguage(): string {
  if (typeof document !== 'undefined') {
    try {
      const stored = window.localStorage.getItem('ccu.language');
      if (stored) return normalizeLang(stored);
    } catch (err) {
      // ignore
    }
    const langAttr = document.documentElement.getAttribute('lang');
    if (langAttr) return normalizeLang(langAttr);
    const root = document.getElementById('app');
    if (root?.dataset?.language) return normalizeLang(root.dataset.language);
  }
  return 'en';
}

export const currentLanguage = ref(resolveInitialLanguage());

export function setLanguage(language: string): void {
  const normalized = normalizeLang(language);
  if (currentLanguage.value === normalized) {
    return;
  }
  currentLanguage.value = normalized;
  if (typeof document !== 'undefined') {
    document.documentElement.setAttribute('lang', normalized);
    const root = document.getElementById('app');
    if (root) root.dataset.language = normalized;
  }
  try {
    window.localStorage.setItem('ccu.language', normalized);
  } catch (err) {
    // ignore
  }
}

if (typeof document !== 'undefined') {
  const observer = new MutationObserver(() => {
    const langAttr = document.documentElement.getAttribute('lang') || '';
    const normalized = normalizeLang(langAttr);
    if (normalized && currentLanguage.value !== normalized) {
      currentLanguage.value = normalized;
    }
  });
  observer.observe(document.documentElement, {
    attributes: true,
    attributeFilter: ['lang'],
  });
}

export function getLanguage(): string {
  return currentLanguage.value;
}

export function t(key: string): string {
  const lang = getLanguage();
  return translations[lang]?.[key] || translations.en?.[key] || key;
}
